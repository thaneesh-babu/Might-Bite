    private static void ingredientsListSearch(String searchTerm) {
        //Looks for elements in the IngredientsList that sart with the search term
        //Sets them into the array matchingIngredients
        //Array is passed to be set as the TempListModel
        String[] matchingIngredients = new String[IngredientsListModel.getSize()];
        int ingNo=0;
        for(int i = 0; i < IngredientsListModel.getSize(); i++) {
            String s = (String) IngredientsListModel.getElementAt(i);
            //System.out.print (",I:"+i+" " + s);
            
            if((s != null) && (s.startsWith(searchTerm) || s.toLowerCase().startsWith(searchTerm)) ) {  
            matchingIngredients[ingNo++] = s;
            }
        
         if(matchingIngredients != null && i==IngredientsListModel.getSize()-1){
            //System.out.println("Passed Array: " + Arrays.toString(matchingIngredients));
            setIngredientsList(matchingIngredients,TempSearchListModel); 
         }
        }
    }
    
