    public static void sortListModel(DefaultListModel TempListModel) {
     String[] Ingredient = new String[TempListModel.getSize()];
     
     for(int i = 0; i< Ingredient.length; i++) {
        Ingredient[i] = (String)TempListModel.getElementAt(i);
        
    }
     
     //Sorting the things in the Array
     String temp;
     
        for ( int i = 0;  i<Ingredient.length; i++) 
        {
            for (int j = i + 1; j < Ingredient.length; j++) 
            {
                if (Ingredient[i].compareTo(Ingredient[j])>0) 
                {
                    temp = Ingredient[i];
                    Ingredient[i] = Ingredient[j];
                    Ingredient[j] = temp;
                }
            }
        }
        TempListModel.clear();
           for( int i = 0; i < Ingredient.length; i++)
        {
             TempListModel.addElement(Ingredient[i]); //Adds each element in the Array to the Model
        }
        try{
            if(TempListModel.contains(IngredientsListModel.elementAt(0)))
               IngredientsList.setModel(TempListModel); //Sets the Model to the List
            if(TempListModel.contains(UserIngredientsListModel.elementAt(0)))
               UserIngredientsList.setModel(TempListModel); //Sets the Model to the List
            if(TempListModel.contains(AvailableRecipesListModel.elementAt(0)))
               AvailableRecipesList.setModel(TempListModel); //Sets the Model to the List
        } catch(Exception e) {}
        
    
    }
    
